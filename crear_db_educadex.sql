DROP DATABASE IF EXISTS educadex;
CREATE DATABASE educadex;
USE educadex;

CREATE TABLE USUARIOS (
    id_usuario INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    rol VARCHAR(20) NOT NULL,
    estatus VARCHAR(20) NOT NULL DEFAULT 'ACTIVO',
    fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE DOCENTES (
    id_docente INT AUTO_INCREMENT PRIMARY KEY,
    clave VARCHAR(20) NOT NULL UNIQUE,
    nombre VARCHAR(50) NOT NULL,
    apellido_paterno VARCHAR(50) NOT NULL,
    apellido_materno VARCHAR(50) NOT NULL,
    correo VARCHAR(100) NOT NULL UNIQUE,
    telefono VARCHAR(15) NOT NULL,
    fecha_nacimiento DATE NOT NULL,
    grado_estudios VARCHAR(50) NOT NULL,
    area_estudios VARCHAR(50) NOT NULL,
    estatus VARCHAR(20) NOT NULL,
    avatar VARCHAR(255)
);

CREATE TABLE ASIGNATURAS (
    id_asignatura INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    clave VARCHAR(20) NOT NULL UNIQUE,
    semestre INT NOT NULL,
    creditos INT NOT NULL
);

CREATE TABLE GRUPOS (
    id_grupo INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL UNIQUE,
    turno VARCHAR(20) NOT NULL,
    capacidad INT NOT NULL
);

CREATE TABLE ALUMNOS (
    id_alumno INT AUTO_INCREMENT PRIMARY KEY,
    matricula VARCHAR(20) NOT NULL UNIQUE,
    semestre INT NOT NULL,
    carrera VARCHAR(50) NOT NULL,
    genero VARCHAR(20) NOT NULL,
    nombre VARCHAR(50) NOT NULL,
    apellido_paterno VARCHAR(50) NOT NULL,
    apellido_materno VARCHAR(50) NOT NULL,
    correo VARCHAR(100) NOT NULL UNIQUE,
    telefono VARCHAR(15) NOT NULL,
    fecha_nacimiento DATE NOT NULL,
    promedio DECIMAL(4,2) NOT NULL,
    estatus VARCHAR(20) NOT NULL,
    id_grupo INT NULL,
    CONSTRAINT alumnos_ibfk_1
    FOREIGN KEY (id_grupo)
    REFERENCES GRUPOS(id_grupo)
    ON DELETE SET NULL
    ON UPDATE CASCADE
);

CREATE TABLE GRUPO_ASIGNATURA (
    id_grupo INT NOT NULL,
    id_asignatura INT NOT NULL,
    id_docente INT NULL,
    PRIMARY KEY (id_grupo, id_asignatura),
    CONSTRAINT grupo_asignatura_ibfk_1
    FOREIGN KEY (id_grupo)
    REFERENCES GRUPOS(id_grupo)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT grupo_asignatura_ibfk_2
    FOREIGN KEY (id_asignatura)
    REFERENCES ASIGNATURAS(id_asignatura)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT grupo_asignatura_ibfk_3
    FOREIGN KEY (id_docente)
    REFERENCES DOCENTES(id_docente)
    ON DELETE SET NULL
    ON UPDATE CASCADE
);

CREATE TABLE EVENTOS (

    id_evento INT AUTO_INCREMENT PRIMARY KEY,

    descripcion VARCHAR(255) NOT NULL,

    fecha TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

